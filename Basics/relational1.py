# Relational Operator

n1 = 11
n2 = 3

print("1. Greater than (>)")
result = n1 > n2
print(f"{n1} > {n2} = {result}")

print()
print("2. Less than (<)")
result = n1 < n2
print(f"{n1} < {n2} = {result}")

print()
print("3. Greater than or equal to (>=)")
result = n1 >= n2
print(f"{n1} >= {n2} = {result}")

print()
print("4. Less than or equal to (<=)")
result = n1 <= n2
print(f"{n1} <= {n2} = {result}")

print()
print("5. double equal to (==)")
result = n1 == n2
print(f"{n1} == {n2} = {result}")

print()
print("6. not equal to (!=)")
result = n1 != n2
print(f"{n1} != {n2} = {result}")